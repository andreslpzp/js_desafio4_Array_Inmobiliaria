const createCard = (property) => {
    const colDiv = document.createElement('div');
    colDiv.className = 'col-md-4 mb-4';

    const cardDiv = document.createElement('div');
    cardDiv.className = 'card';

    const img = document.createElement('img');
    img.src = property.src;
    img.className = 'card-img-top';
    img.alt = 'Imagen del departamento';
    img.style.height = '40%';
    img.style.width = '100%';
    img.style.objectFit = 'cover';

    const cardBodyDiv = document.createElement('div');
    cardBodyDiv.className = 'card-body';

    const h5 = document.createElement('h5');
    h5.className = 'card-title';
    h5.textContent = property.h5;

    const description = document.createElement('p');
    description.className = 'card-text';
    description.textContent = property.p;
    description.style.color = 'black';

    const location = document.createElement('p');
    location.innerHTML = `<i class="fas fa-map-marker-alt"></i> ${property.i1}`;
    location.style.color = 'black';

    const details = document.createElement('p');
    details.innerHTML = `<i class="fas fa-bed"></i> ${property.i2} Habitaciones | <i class="fas fa-bath"></i> ${property.i3} Baños`;
    details.style.color = 'black';

    const price = document.createElement('p');
    price.innerHTML = `<i class="fas fa-dollar-sign"></i> ${property.i4}`;
    price.style.color = 'black';

    const smoking = document.createElement('p');
    smoking.className = property.i5.includes('Permitido fumar') ? 'text-success' : 'text-danger';
    smoking.innerHTML = `<i class="${property.i5.includes('Permitido fumar') ? 'fas fa-smoking' : 'fas fa-smoking-ban'}"></i> ${property.i5}`;
    smoking.style.color = 'black';

    const pets = document.createElement('p');
    pets.className = property.i6.includes('Mascotas permitidas') ? 'text-success' : 'text-danger';
    pets.innerHTML = `<i class="${property.i6.includes('Mascotas permitidas') ? 'fas fa-paw' : 'fa-solid fa-ban'}"></i> ${property.i6}`;
    pets.style.color = 'black';

    cardBodyDiv.appendChild(h5);
    cardBodyDiv.appendChild(description);
    cardBodyDiv.appendChild(location);
    cardBodyDiv.appendChild(details);
    cardBodyDiv.appendChild(price);
    cardBodyDiv.appendChild(smoking);
    cardBodyDiv.appendChild(pets);

    cardDiv.appendChild(img);
    cardDiv.appendChild(cardBodyDiv);
    colDiv.appendChild(cardDiv);

    return colDiv;
};

const renderProperties = (propertiesArray, targetElement) => {
    propertiesArray.forEach(property => {
        const card = createCard(property);
        targetElement.appendChild(card);
    });
};

document.addEventListener('DOMContentLoaded', () => {
    const rowDiv = document.querySelector('.row');
    renderProperties(Propiedadesenventa, rowDiv);
});

document.addEventListener('DOMContentLoaded', () => {
    const rowDiv = document.querySelector('.row');
    renderProperties(PropiedadesenAlquiler, rowDiv);
});


function generatePropertyCards(properties) {
    return properties.map(property => `
        <div class="col-md-4 mb-4">
            <div class="card">
                <img src="${property.src}" class="card-img-top" alt="Imagen del departamento"/>
                <div class="card-body">
                    <h5 class="card-title">${property.h5}</h5>
                    <p class="card-text">${property.p}</p>
                    <p><i class="fas fa-map-marker-alt"></i> ${property.i1}</p>
                    <p><i class="fas fa-bed"></i> ${property.i2} Habitaciones | <i class="fas fa-bath"></i> ${property.i3} Baños</p>
                    <p><i class="fas fa-dollar-sign"></i> ${property.i4}</p>
                    <p class="${property.i5.includes('No') ? 'text-danger' : 'text-success'}"><i class="fas fa-smoking${property.i5.includes('No') ? '-ban' : ''}"></i> ${property.i5}</p>
                    <p class="${property.i6.includes('No') ? 'text-danger' : 'text-success'}"><i class="fas fa-paw"></i> ${property.i6}</p>
                </div>
            </div>
        </div>
    `).join('');
}

document.addEventListener('DOMContentLoaded', () => {
    const ventaContainer = document.querySelector('#venta .row');
    if (ventaContainer) {
        ventaContainer.innerHTML = generatePropertyCards(Propiedadesenventa);
    }
});

document.addEventListener('DOMContentLoaded', () => {
    const ventaContainer = document.querySelector('#alquiler .row');
    if (ventaContainer) {
        ventaContainer.innerHTML = generatePropertyCards(PropiedadesenAlquiler);
    }
});